document.addEventListener('DOMContentLoaded', () => {
    const list = document.getElementById('random-posts');
    if(!list) return;

    // 1. 配列をシャッフル（ランダムに混ぜる）
    const shuffled = [...blogPosts].sort(() => 0.5 - Math.random());

    // 2. 最初の3つを取り出す
    const selected = shuffled.slice(0, 3);

    // 3. HTMLを生成して埋め込む
    let html = '';
    selected.forEach(post => {
        html += `
            <li style="margin-bottom: 10px;">
                <a href="${post.url}">
                    <span lang="ja">${post.ja}</span>
                    <span lang="en">${post.en}</span>
                </a>
            </li>
        `;
    });

    // 4. トップページへの戻るリンクを最後に追加
    html += `
        <li style="margin-top: 15px; border-top: 1px dashed #eee; padding-top: 10px;">
            <a href="../index.html" style="font-weight:bold;">
                <span lang="ja">トップページへ戻る</span>
                <span lang="en">Back to Top</span>
            </a>
        </li>
    `;

    list.innerHTML = html;
});
